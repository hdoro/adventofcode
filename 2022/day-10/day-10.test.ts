const minimalExample = `
noop
addx 3
addx -5`

test('Parsing cycles', () => {
  expect(parseCyclesStartEnd(minimalExample)).toStrictEqual([
    { start: 1, end: 1 },
    { start: 1, end: 1 },
    { start: 1, end: 4 },
    { start: 4, end: 4 },
    { start: 4, end: -1 },
  ])
  expect(parseCycles(minimalExample)).toStrictEqual([1, 1, 4, 4, -1])
})

